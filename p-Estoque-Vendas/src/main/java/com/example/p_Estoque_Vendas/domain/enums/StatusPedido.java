package com.example.p_Estoque_Vendas.domain.enums;

public enum StatusPedido {

    REALIZADO,
    CANCELADO;

}

// Essa é uma enumeração chamada StatusPedido que define dois valores: REALIZADO e CANCELADO.
// Enums são usados para representar conjuntos fixos de constantes. Nesse caso, a enumeração
// representa os possíveis estados que um pedido pode ter: "REALIZADO" ou "CANCELADO".



//Enumerações (enum) em Java:
//
//  Definição de Tipo:
//      Um enum é um tipo de dado em Java que representa um conjunto fixo de constantes
//      nomeadas. Essas constantes representam valores possíveis para uma variável.
//
//  Valores Fixos:
//      Enums são usados para definir um conjunto fixo e limitado de valores que uma variável
//      pode ter. Esses valores são chamados de "enumeradores".
//
//  Leitura mais Clara:
//      O uso de enums torna o código mais claro e legível, pois você pode dar nomes
//      descritivos aos valores possíveis.
//
//  Segurança de Tipo:
//      Enums fornecem segurança de tipo, o que significa que você não pode atribuir valores
//      que não são parte do conjunto fixo de enumerações.
//
//  Acessíveis Globalmente:
//      Enums podem ser acessados de forma global em um programa Java. Você pode referenciar
//      as constantes do enum em qualquer parte do seu código.
//
//  Implementação de Métodos:
//      Enums podem ter métodos associados a eles, permitindo comportamento personalizado
//      para cada constante do enum.
//
//  Em resumo, um enum em Java é uma maneira de criar tipos de dados que representam um
//  conjunto definido e limitado de valores constantes. Isso é útil quando você tem um
//  conjunto específico de opções ou estados que uma variável pode ter e deseja tornar o
//  código mais compreensível e seguro. Enums são amplamente utilizados para melhorar a
//  clareza e a manutenção do código, especialmente em situações em que você tem um conjunto
//  fixo de valores que representam categorias, estados ou opções.
